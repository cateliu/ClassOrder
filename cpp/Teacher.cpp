//查看所有预约
void Teacher::showAllorder()
{
	orderFile of;
	if (of.m_Size == 0)
	{
		cout << "无预约！" << endl;
		system("pause");
		system("cls");
		return;
	}

	for (int i = 0; i < of.m_Size; i++)
	{
		cout <<  i + 1;
		cout << ", 预约日期：周" << of.m_orderData[i]["date"];
		cout << " 时段：" << (of.m_orderData[i]["interval"] == "1" ? "上午" : "下午");
		cout << " 机房：" << of.m_orderData[i]["roomId"];
		cout << " 学号：" << of.m_orderData[i]["stuId"];
		cout << " 姓名：" << of.m_orderData[i]["stuName"];
		string status = " 状态："; // 0 取消预约，1 审核中，2 已预约， -1 预约失败
		if (of.m_orderData[i]["status"] == "1")
		{
			status += "审核中";
		}
		else if (of.m_orderData[i]["status"] == "0")
		{
			status += "取消预约";
		}
		else if (of.m_orderData[i]["status"] == "2")
		{
			status += "已预约";
		}
		else
		{
			status += "预约失败";
		}
		cout << status << endl;
	}
	system("pause");
	system("cls");
}

//审核预约
void Teacher::validOrder()
{
	orderFile of;
	if (of.m_Size == 0)
	{
		cout << "无预约！" << endl;
		system("pause");
		system("cls");
		return;
	}
	cout << "待审核的预约如下：" << endl;

	int index = 0;
	vector<int> v;
	for (int i = 0; i < of.m_Size; i++)
	{
		if (of.m_orderData[i]["status"] == "1")
		{
			v.push_back(i);
			cout << index + 1;
			cout << ", 预约日期：周" << of.m_orderData[i]["date"];
			cout << " 时段：" << (of.m_orderData[i]["interval"] == "1" ? "上午" : "下午");
			cout << " 机房：" << of.m_orderData[i]["roomId"];
			cout << " 学号：" << of.m_orderData[i]["stuId"];
			cout << " 姓名：" << of.m_orderData[i]["stuName"];
			cout << " 状态：审核中" << endl;
			index++;
			
		}
	}
	int select;
	int ret;
	while (true)
	{
		cout << "请输入待审核的申请，输入0退回原界面！"<< endl;
		cin >> select;
		
		if (select > 0 && select <= v.size())
		{
			cout << "请输入审核结果：" << endl;
			cout << "1. 通过" << endl;
			cout << "2. 不通过" << endl;
			cin >> ret;
			if (ret == 1)
			{
				of.m_orderData[v[select - 1]]["status"] = "2";
			}
			else
			{
				of.m_orderData[v[select - 1]]["status"] = "-1";
			}
			of.updataOrder();
			cout << "审核完毕！" << endl;
			break;
		}
		else if (select == 0)
		{
			break;
		}
		else
		{
			cout << "输入有误！请重新输入。" << endl;
		}
	}
}